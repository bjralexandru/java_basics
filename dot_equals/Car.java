public class Car {

    private int gears;
    private String brand;
    private int revisionPeriod;

    public int getGears() {
        return gears;
    }

    public void setGears(int gears) {
        this.gears = gears;
    }

    public int getRevisionPeriod() {
        return revisionPeriod;
    }

    public void setRevision(int revisionPeriod) {
        this.revisionPeriod = revisionPeriod;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }
}
